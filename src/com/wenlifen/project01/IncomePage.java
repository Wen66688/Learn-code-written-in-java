package com.wenlifen.project01;

public class IncomePage {
	private double num;
	private String str;

	public IncomePage(double num, String str) {
		super();
		this.num = num;
		this.str = str;
	}
	public double getNum() {
		return num;
	}
	public void setNum(double num) {
		this.num = num;
	}
	public String getStr() {
		return str;
	}
	public void setStr(String str) {
		this.str = str;
	}

	
}
